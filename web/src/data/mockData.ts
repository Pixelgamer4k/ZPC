export function getAgentById(id: string): Agent | undefined {
  return agents.find((a) => a.id === id);
}

export function getBusinessById(id: string): Business | undefined {
  return businesses.find((b) => b.id === id);
}

export function getTasksForBusiness(businessId: string): Task[] {
  return tasks.filter((t) => t.businessId === businessId);
}

export function getDirectReports(managerId: string): Agent[] {
  return agents.filter((a) => a.managerId === managerId);
}
